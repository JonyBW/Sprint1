/*Creando la base de datos*/

DROP DATABASE IF EXISTS Betisware;
CREATE DATABASE IF NOT EXISTS Betisware;
USE Betisware;


/*Creando las tablas*/

CREATE TABLE PROYECTOS(
    codProyecto int(5) NOT NULL,
    Nombre varchar(40) NOT NULL UNIQUE,
    Presupuesto_Restante decimal(12,2) NOT NULL DEFAULT 0,
    Fecha_Iniciacion date NOT NULL,
    Fecha_Limite date
);

CREATE TABLE TALLERES(
    codTaller int(3) NOT NULL,
    Estado enum('cerrado', 'abierto', 'en_mantenimiento', 'en_inspeccion') NOT NULL DEFAULT 'cerrado',
    Hora_Apertura time NOT NULL,
    Hora_Cierre time NOT NULL,
    Fecha_Inspeccion date NOT NULL
);

CREATE TABLE DIRECCIONES(
    codDireccion int(6) NOT NULL,
    Direccion varchar(40) NOT NULL UNIQUE
);

CREATE TABLE AMBITOS(
    codAmbito int(2) NOT NULL,
    Ambito varchar(40) NOT NULL UNIQUE
);

CREATE TABLE INVERSORES(
    codInversor int(3) NOT NULL,
    Nombre varchar(20),
    Apellido1 varchar(30),	
    Apellido2 varchar(30),
    Cant_Invertida decimal(11,2) NOT NULL DEFAULT 0,
    codDireccion int(6),
    Correo_Electronico varchar(30) UNIQUE,
    nTel varchar(13) UNIQUE
);

CREATE TABLE CUENTAS(
    UID int(6) NOT NULL,
    Nombre varchar(20),
    Apellido1 varchar(30),
    Apellido2 varchar(30),
    codDireccion int(6),
    Correo_Electronico varchar(30) UNIQUE,
    nTel varchar(13) UNIQUE,
    nomUsuario varchar(20) NOT NULL,
    Contraseña varbinary(255) NOT NULL
);

CREATE TABLE PERTENENCIA_AMBITOS_PROYECTOS(
    codProyecto int(5) NOT NULL,
    codAmbito int(2) NOT NULL
);

CREATE TABLE MATERIALES(
    codMaterial int(4) NOT NULL,
    Nombre varchar(100) NOT NULL,
    codTaller int(3) NOT NULL,
    Cant_Total int(4) NOT NULL DEFAULT 0,
    Cant_Disponible int(4) NOT NULL DEFAULT 0
);

CREATE TABLE INVERSIONES(
    codInversor int(3) NOT NULL,
    codProyecto int(5) NOT NULL,
    Dinero decimal(12,2) NOT NULL,
    Fecha date NOT NULL,
    Hora time NOT NULL
);

CREATE TABLE IDEAS(
    codIdea int(6) NOT NULL,
    Nombre varchar(40) NOT NULL,
    UID int(6) NOT NULL,
    Estado enum('sin_inspeccionar', 'en_inspeccion', 'en_espera', 'en_preparacion') NOT NULL DEFAULT 'sin_inspeccionar',
    Fecha_Creacion date NOT NULL,
    Puntuacion_Total int(3),
    Puntuacion_Social int(3),
    Puntuacion_Medioambiental int(3),
    Puntuacion_Economica int(3)
);

CREATE TABLE PERFILES(
    codPerfil int(2) NOT NULL,
    Perfil varchar(30) NOT NULL UNIQUE
);

CREATE TABLE PROYECTOS_TALLERES(
    codProyecto int(5) NOT NULL,
    codTaller int(3) NOT NULL,
    Fecha_Asignacion date NOT NULL,
    Horas_Invertidas decimal(6,2) NOT NULL DEFAULT 0
);

CREATE TABLE INSPIRACIONES_PROYECTOS(
    codIdea int(6) NOT NULL,
	codProyecto int(5) NOT NULL
);

CREATE TABLE EMPLEADOS(
    codEmpleado int(4) NOT NULL,
    Nombre varchar(20) NOT NULL,
    Apellido1 varchar(30) NOT NULL,
    Apellido2 varchar(30) NOT NULL,
    codSeguridad_Social int(12) NOT NULL UNIQUE,
    Fecha_Entrada date NOT NULL,
    Salario_Anual decimal(9,2) NOT NULL,
    Fecha_Nacimiento date NOT NULL,
    codPerfil int(2) NOT NULL,
    Dias_Vacaciones_Restantes int(3) NOT NULL,
    codDireccion int(6) NOT NULL,
    Correo_Electronico varchar(30) UNIQUE,
    codHuella int(4) NOT NULL UNIQUE,
    nTel varchar(13) UNIQUE
);

CREATE TABLE PROYECTOS_EMPLEADOS(
    codEmpleado int(4) NOT NULL,
    codProyecto int(5) NOT NULL,
    Fecha_Asignacion date NOT NULL,
    Horas_Invertidas decimal(6,2) NOT NULL DEFAULT 0
);

CREATE TABLE PERTENENCIA_AMBITOS_IDEAS(
    codIdea int(6),
    codAmbito int(2)
);

CREATE TABLE HUELLAS(
    codHuella int(4) NOT NULL,
    Huella varbinary(255) NOT NULL UNIQUE
);


