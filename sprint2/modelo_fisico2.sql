ALTER TABLE PERTENENCIA_AMBITOS_IDEAS ADD CONSTRAINT FK_PERTENENCIA_AMBITOS_IDEAS_ambitos FOREIGN KEY (codAmbito) REFERENCES AMBITOS(codAmbito);